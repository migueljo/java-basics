package platzibasic.conditionals;

public class LogicOperations {
	public static void main(String[] args) {
		var a = 8;
		var b = 5;
		System.out.println("a > b? " + (a > b));
		System.out.println("a < b? " + (a < b));
		System.out.println("a === b? " + (a == b));
		System.out.println("a != b? " + (a != b));
	}
}
